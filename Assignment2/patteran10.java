class Patteran10
{
	public static void main(String args[])
	{
		for(int i=4;i>=0;i--)
			
		{
			 char  ch='A';
			for(int j=0;j<i;j++)
			{
				
				System.out.print(" ");
			}
			
			for(int k=i;k<=4;k++)
			{
				System.out.print ((ch+k) + " ");
				
			}
			
				
			System.out.println();
		}
	}
}